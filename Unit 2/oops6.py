#Operator Polymorphism

# Plus Operator
print(10+20)
print("Hello "+"World")
print([1,2,3]+[4,5,6])
print((1,2,3)+(4,5,6))
#Multiplication Operator
print(10*20)
print("Hello "*3)
print([1,2,3]*3)
print((1,2,3)*3)

#User Defined Function Polymorphism
print(len("Hello"))
print(len([10,20,30,30,20]))
print(len({10,20,30,30,20})) #In set repetitions are removed